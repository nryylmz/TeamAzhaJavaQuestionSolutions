package Marsela2;

public class SwapNumbers {
    public static void main(String[] args) {

        //declare 2 int variables

        int x = 10;
        int y = 15;

        // use arithmetic operation do swap value
        x = x + y;
        y = x - y;
        x = x - y;


        System.out.println("x = " + x);

        System.out.println("y = " + y);
    }
}

